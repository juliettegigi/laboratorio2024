CREATE TABLE usuarios(
    id INT PRIMARY KEY AUTO_INCREMENT,
    pass VARCHAR(255),
    nickName varchar(20) unique,
    email VARCHAR(255) UNIQUE,
    nombre VARCHAR(255),
    apellido VARCHAR(55),
    documento VARCHAR(255) UNIQUE,
    createdAt DATETIME DEFAULT NOW(),
    updatedAt DATETIME DEFAULT NOW(),
    deletedAt DATETIME DEFAULT NULL
);



CREATE TABLE usuarioRoles(
    id INT PRIMARY KEY AUTO_INCREMENT,
    usuarioId INT NOT NULL,
    rolId INT NOT NULL,
    createdAt DATETIME DEFAULT NOW(),
    updatedAt DATETIME DEFAULT NOW(),
    deletedAt DATETIME DEFAULT NULL,
    FOREIGN KEY(usuarioId) REFERENCES usuarios(id),
    FOREIGN KEY(rolId) REFERENCES roles(id)
);



CREATE TABLE pacientes(
    id INT PRIMARY KEY AUTO_INCREMENT,
    usuarioId int not null unique,
    edad tinyInt unsigned,
    nacimiento date,
    embarazada bit,
    provincia varchar(100),
    localidad varchar(100),
    direccion varchar(100),
	sexo ENUM('Masculino', 'Femenino', 'Otro', 'Prefiero no decirlo'),
    createdAt datetime DEFAULT NOW(),
updatedAt datetime DEFAULT NOW(),
deletedAt datetime DEFAULT NULL,
    FOREIGN KEY(usuarioId) REFERENCES usuarios(id)
);

CREATE TABLE telefonos (
    id INT PRIMARY KEY AUTO_INCREMENT,
    usuarioId INT NOT NULL,
    numero VARCHAR(20),
    descripcion tinytext,
    FOREIGN KEY (usuarioId) REFERENCES usuarios(id)
);




CREATE TABLE ordenes(
id INT PRIMARY KEY AUTO_INCREMENT,
pacienteId int,
medicoId int,
diagnosticoId int,
isPresuntivo bit,
estadoId int,
fecha DATETIME default NOW(),  -- puede que la fecha sea distinta a la fecha en la que se creó el registro,por ejemplo: registré hoy lo que pasó ayer
fechaEntrega DATETIME DEFAULT NULL,  -- de todos los resultados
tiempoDeProcesamiento int, 
isUrgente bit,
createdAt DATETIME DEFAULT NOW(),
    updatedAt DATETIME DEFAULT NOW(),
    deletedAt DATETIME DEFAULT NULL,
FOREIGN KEY(pacienteId) REFERENCES pacientes(id),   
FOREIGN KEY(medicoId) REFERENCES medicos(id),   
FOREIGN KEY(diagnosticoId) REFERENCES diagnosticos(id),
FOREIGN KEY(estadoId) REFERENCES estados(id)
);

